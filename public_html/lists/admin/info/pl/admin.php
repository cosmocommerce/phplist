<p>Ustaw uprawnienia, aby przeglądać strony w systemie:</p>
<ul>
<li>Wszystko: admin ma dostęp do stony bez żadnych ograniczeń</li>
<li>Podgląd: admin może ogladać zawartość stron, ale nie może zmieniać niczego. Obecnie działa to
dla stron "użytkownika", "użytkowników" i "członków".</li>
<li>Brak: admin nie może oglądać tej strony</li>
<li>Właściciel: admin może oglądać tą stronę, ale widzi tylko zawartość list, których jest właścicielem</li>
</ul>
