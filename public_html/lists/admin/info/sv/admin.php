<p>Ställ in befogenheter för att se sidor i systemet:</p>
<ul>
<li>Alla: administratören kan se sidan utan begränsningar</li>
<li>Visa: administratören kan se sidinnehållet men inte ändra något. Detta fungerar för närvarande bara
för "användar"-, "användare"- och "medlemmar"-sidorna.</li>
<li>Inga: administratören kan inte se den här sidan</li>
<li>Ägare: administratören kan se den här sidan men ser bara innehållet på listorna denne själv är ägare till</li>
</ul>
