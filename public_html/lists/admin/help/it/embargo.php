<b>Ferma un messaggio</b>
<p>Puoi fermare un messaggio per fare in modo che non sia inviato prima della data e ora che indichi.
Di default sar&aacute; inviato oggi alle ore 0:00, e quindi immediatamente. </p>
<p><b>Note bene</b>: il fermo imposta il momento di inizio nella spedizione del messaggio.
Questo però non garantisce che i messaggi arriveranno nella caselle di posta degli utenti a quell'ora.</p>
