<p>Annotazioni per unione attributi.</p>
<p>Unire gli attributi significa che i valori degli utenti rimarranno gli stessi, ma che gli attuali attributi vengono uniti su uno solamente. Gli attributi che sono mantenuti sono i primi (tramite lista ordinamento, come puoi vedere nella pagina).</p>
<ul>
<li>Puoi solo unire attributi che sono dello stesso tipo</li>
<li>Quando unisci, il valore del primo attributo viene mantenuto se esiste, altrimenti viene sovrascritto dal valore dell'attributo che gli viene unito. Questo pu&ograve; causare perdita di dati, nel caso che entrambi gli attributi hanno un valore.</li>
<li>Se unisci attributi del tipo <i>checkbox</i> l'attributo risultante dall'unione diventa di tipo <i>checkboxgroup</i>.</li>
<li>Gli attributi correttamente uniti su un'altro vengono cancellati dopo l'unione.</li>
</ul>
